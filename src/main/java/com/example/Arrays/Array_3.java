package com.example.Arrays;

import java.util.Arrays;

public class Array_3 {
    public static void main(String[] args) {
        int[] iArr = {1,2,3,4,5};
        for(int i=0;i<iArr.length;i++){
            System.out.println(iArr[i]);
        }

        //[1, 2, 3, 4, 5]
        System.out.println(Arrays.toString(iArr));

    }
}
